"""
	En pantalla se presenta el resultado de algunas operaciones booleanas
	como:
	<    <-- menor que
	>    <-- mayor que
	<=   <-- menor igual que
	>=   <-- mayor igual que
	==   <-- igual
	1=   <-- diferente
"""
print(10 > 3)
print(10 > 11)
print(10 <= 10)
print(10 < 2)
print(10 == 10)
print(10 != 2)